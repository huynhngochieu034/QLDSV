    public void removeStudentExits() {
        for (int i = 0; i < listsv.size(); i++) {
            if (listsv.get(i).isNGHIHOC()) {
                listsv.remove(i);
            }
        }
    }

    public boolean checkExitsStudent() {
        try {
            String check = "";
            for (int i = 0; i < listsv.size(); i++) {
                check = diemDAO.timDiemPROC(listsv.get(i).getMASV(), txtMaMon.getText(), 1);
                if (check == null) {
                    return false;
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
        return true;
    }

