    private void jButtonBatDauActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBatDauActionPerformed

        try {
            // TODO add your handling code here:
            // show dialog
            listsv = sinhvienDAO.timSinhVienTheoLop((String) jComboBoxMaLop.getSelectedItem());
            removeStudentExits();
            if (jComboBoxLan.getSelectedItem().equals("2") && checkExitsStudent() == false) {
                JOptionPane.showMessageDialog(rootPane, "Lớp có sinh viên chưa nhập điểm của môn học này lần 1");
                return;
            } else if (listsv.size() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Lớp này không có sinh viên nào");
                return;
            }
            DiemUPDATE diemUPDATE = new DiemUPDATE(this, true, (String) jComboBoxMaLop.getSelectedItem(), txtMaMon.getText(), Integer.parseInt((String) jComboBoxLan.getSelectedItem()), listsv);
            diemUPDATE.setVisible(true);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }


    }//GEN-LAST:event_jButtonBatDauActionPerformed

