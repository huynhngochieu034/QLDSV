    private void populateGUI(SinhVien sv) {
        txtMASV.setText(sv.getMASV());
        txtHo.setText(sv.getHO());
        txtTen.setText(sv.getTEN());
        jComboBoxMaLop.setSelectedItem(sv.getMALOP());
        if (sv.isPHAI() == true) {
            jRadioButtonNam.setSelected(true);
        } else {
            jRadioButtonNu.setSelected(true);
        }

        txtNgaySinh.setText(sv.getDobString());
        txtNoiSinh.setText(sv.getNOISINH());
        txtDiaChi.setText(sv.getDIACHI());
        jTextAreaGhichu.setText(sv.getGHICHU());
        if (sv.isNGHIHOC() == true) {
            jRadioButtonCo.setSelected(true);
        } else {
            jRadioButtonKhong.setSelected(true);
        }
        SVStack = sv;
    }

