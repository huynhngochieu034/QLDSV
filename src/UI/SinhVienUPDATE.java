    protected void saveSinhVien() {

        try {

            // get the student info from gui
            String MASV = txtMASV.getText();
            String Ho = txtHo.getText();
            String Ten = txtTen.getText();
            String MaLop = (String) jComboBoxMaLop.getSelectedItem();
            boolean Phai = isPhai();
            String NgaySinhString = txtNgaySinh.getText();
            Date NgaySinh = (Date) sdf.parse(NgaySinhString);
            String NoiSinh = txtNoiSinh.getText();
            String DiaChi = txtDiaChi.getText();
            String GhiChu = jTextAreaGhichu.getText();
            boolean NghiHoc = isNghiHoc();

            SinhVien tempSinhVien = null;

            if (!updateMode) {
                tempSinhVien = new SinhVien(MASV, Ho, Ten, MaLop, Phai, NgaySinh, NoiSinh, DiaChi, GhiChu, NghiHoc);
            } else {
                tempSinhVien = new SinhVien(previousStudent.getMASV(), Ho, Ten, MaLop, Phai, NgaySinh, NoiSinh, DiaChi, GhiChu, NghiHoc);
            }
            try {
                // save to the database
                if (updateMode) {
                    sinhvienDAO.capNhatSinhVienPROC(tempSinhVien);
                    sinhvienGUI.stringSV.push("CapNhat");
                    sinhvienGUI.stackSV.push(SVStack);
                } else {
                    sinhvienDAO.themSinhVienPROC(tempSinhVien);
                    sinhvienGUI.stringSV.push("Them");
                    sinhvienGUI.stackSV.push(tempSinhVien);
                }
                // close dialog
                setVisible(false);
                dispose();
                // refresh gui list
                sinhvienGUI.displaySinhVien();

                // show success message
                if (!updateMode) {
                    JOptionPane.showMessageDialog(sinhvienGUI,
                            "Thêm sinh viên thành công",
                            "",
                            JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(sinhvienGUI,
                            "Cập nhật sinh viên thành công.",
                            "",
                            JOptionPane.INFORMATION_MESSAGE);

                }
            } catch (Exception exc) {
                JOptionPane.showMessageDialog(
                        sinhvienGUI,
                        "Lỗi lưu sinh viên: "
                        + exc.getMessage(), "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đúng ngày");
        }
    }

