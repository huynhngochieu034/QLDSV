    private void btnPhucHoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPhucHoiActionPerformed
        // TODO add your handling code here:

        if (strings.isEmpty() && stack.isEmpty()) {
            return;
        } else {
            String check = strings.pop();
            if (check.equals("Them")) {
                try {
                    int response = JOptionPane.showConfirmDialog(
                            this, "Bạn có muốn xóa lớp vừa thêm không?", "Confirm",
                            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                    if (response != JOptionPane.YES_OPTION) {
                        return;
                    }
                    // get the current 
                    Lop tempLop = stack.pop();
                    // delete the student
                    lopDAO.xoaLopPROC(tempLop.getMALOP());
                    // refrprocessesh GUI
                    danhSachLop();
                } catch (SQLException ex) {
                    JOptionPane.showConfirmDialog(rootPane, ex.getMessage());
                }
            } else if (check.equals("CapNhat")) {
                try {
                    Lop tempLop = stack.pop();
                    // delete the student
                    lopDAO.capNhatLopPROC(tempLop);
                    // refrprocessesh GUI
                    danhSachLop();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(rootPane, ex);
                }
            } else if (check.equals("Xoa")) {
                try {
                    Lop tempLop = stack.pop();
                    // delete the student
                    lopDAO.themLopPROC(tempLop);
                    //refrprocessesh GUI
                    danhSachLop();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(rootPane, ex);
                }
            }

        }
    }//GEN-LAST:event_btnPhucHoiActionPerformed

