    public void danhSachLop() {
        try {
            lopDAO = new LopDAO();
            ArrayList<Lop> dsLop = null;
            dsLop = lopDAO.findAll();
            LopTableModel model = new LopTableModel(dsLop);
            jTableLop.setModel(model);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }

    }

    public void capNhatLop() throws SQLException {

        // get the selected item
        int row = jTableLop.getSelectedRow();

        // make sure a row is selected
        if (row < 0) {
            JOptionPane.showMessageDialog(rootPane, "Bạn phải chọn lớp");
            return;
        }

        // get the current student
        Lop tempLop = (Lop) jTableLop.getValueAt(row, LopTableModel.OBJECT_COL);

        // create dialog
        LopUPDATE dialog = new LopUPDATE(this, lopDAO, tempLop, true);
        // show dialog
        dialog.setVisible(true);
    }

    public void xoaLop() {

        try {
            // get the selected row
            int row = jTableLop.getSelectedRow();

            // make sure a row is selected
            if (row < 0) {
                JOptionPane.showMessageDialog(this,
                        "Bạn phải chọn Lớp", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // prompt the user
            int response = JOptionPane.showConfirmDialog(
                    this, "Bạn có muốn xóa lớp này không?", "Confirm",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (response != JOptionPane.YES_OPTION) {
                return;
            }

            // get the current student
            Lop tempLop = (Lop) jTableLop.getValueAt(row, LopTableModel.OBJECT_COL);
            // delete the student
            lopDAO.xoaLopPROC(tempLop.getMALOP());
            strings.push("Xoa");
            stack.push(tempLop);
            // refrprocessesh GUI
            danhSachLop();

            // show success message
            JOptionPane.showMessageDialog(this,
                    "Xóa lớp thành công.", "Deleted",
                    JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception exc) {
            JOptionPane.showMessageDialog(this,
                    "Lỗi xóa lớp: " + exc.getMessage(), "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }


